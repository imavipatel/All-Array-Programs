//WAP to iterate an array using for loop.
public class ArrayIterateUsingFor{
  public static void main(String[] args) {
    int arr[];
    arr = new int[]{35,45,25,30};
    //int [] arr = new int[]{35,45,25,30,50,10};
    for(int i=0;i<arr.length;i++)
    {
      System.out.print(arr[i]+" ");
    }
  }
}
